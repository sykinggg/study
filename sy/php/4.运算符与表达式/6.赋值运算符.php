赋值运算符
基本概念:赋值运算符串是把基本赋值运算符（“=”）右边的值班赋给左边的变量或者常量
操作			符号		实例			展开形式		意义
赋值			=		$A=B 		$A=3 		 将右边的赋值给左边
加			+=		$A+=B		$A=$A+B		将右边的值加到左边
减			-=		$A-=B		$A=$A-B		将右边的值减到左边
乘			*=		$A*=B		$A=$A*B		将右边的值乘以左边的
除			/=		$A/=B		$A=$A/B		将右边的值除以左边的
连接字符		.=		$A.=B		$A=$A.B		将右边的字符串加到左边
取余数		%=		$A%=B		$A=$A%B		将左边的值对右边取余数